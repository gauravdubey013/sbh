import mongoose from "mongoose";

const { Schema } = mongoose;

const contactUsMessageSchema = new Schema(
  {
    firstname: {
      type: String,
      required: true,
    },
    lastname: {
      type: String,
      required: true,
    },
    email: {
      unique: true,
      type: String,
      required: true,
    },
    message: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

export default mongoose.models.ContactUsMessage ||
  mongoose.model("ContactUsMessage", contactUsMessageSchema);
